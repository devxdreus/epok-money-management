    private void TambahkanIsiTable(){
        DefaultTableModel kolomkolom = new DefaultTableModel();
        kolomkolom.addColumn("Kategori");
        kolomkolom.addColumn("Digunakan");
        kolomkolom.addColumn("Total");
        TableAnggaran.setModel(kolomkolom);
        
        List<Map<String, String>> budgets = Budget.getAll();
        
        budgets.forEach(budget -> {
            kolomkolom.addRow(new Object[]{
                budget.get("category_name"),
                budget.get("used"),
                budget.get("total")
        });
        });
        
        
    }

    
